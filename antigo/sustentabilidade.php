<span style="color:#333; font:12px/17px Arial, Helvetica, sans-serif;">
O Hospital Nossa Senhora da Piedade tem investido em ações focadas em sustentabilidade, criando iniciativas voltadas à conscientização e adoção de práticas sustentáveis. As campanhas, que levam em consideração todas as premissas do conceito (ambiental, social e econômico), têm como objetivo unir pacientes, público interno e colaboradores em prol da adoção de novos hábitos para a preservação do meio ambiente.
<br /><br />
Em conjunto com a área de Segurança do Trabalho, a comissão de Gerenciamento de Resíduos do Hospital Nossa Senhora da Piedade criou um programa para proteger os colaboradores dos riscos de acidentes de trabalho no manuseio do lixo e de materiais de descarte. Esta iniciativa também tem como foco colaborar com o meio ambiente por meio da reciclagem, tendo como meta reciclar 20% dos resíduos gerados comuns recolhidos no hospital. Cartazes para orientação de descarte adequado e seguro foram afixados em todas as dependências que possuem lixeiras identificadas para a coleta seletiva.
<br /><br />
Uma série de atividades ambientais, como troca de aparelhos de ar condicionado, implantação de lâmpadas LED, entre outras, estão sendo colocadas em práticas no hospital. Em 2010, foram reciclados aproximadamente 19% dos resíduos gerados de materiais que deixaram de ser depositados em aterro sanitário.
<br /><br />
Na área da Tecnologia da Informação foram substituídos monitores de aproximadamente 50 computadores, o que também trouxe redução no consumo de energia e mudança na tecnologia dos aparelhos de Raio X, ressonância e tomografia, reduzindo substancialmente a utilização de produtos químicos usados para a impressão das chapas. 
<br /><br />
<h1>Dicas para contribuir com a redução do desperdício de recursos naturais</h1>
<br /><br />
- Evite o desperdício de papel e contribua para a redução do corte de árvores e do lançamento dos gases que formam o efeito estufa. Use o outro lado dos papéis como rascunho;
<br /><br />
- Só 2,5% da água do Planeta Terra é doce e, destes, somente 0,01% é utilizável pelo homem. Portanto, utilize somente a água necessária, fechando a torneira enquanto escova os dentes ou ensaboa as louças, por exemplo;
<br /><br />
- Verifique regularmente se há vazamentos de água em sua casa. Pode estar havendo desperdício sem que você que saiba;
<br /><br />
- Em caso de vazamento nas ruas e calçadas, ligue gratuitamente 195 (Sabesp) para que o problema possa ser resolvido o mais rápido possível;
<br /><br />
- Colete água da chuva. Esta água pode ser reutilizada para diversas ações do cotidiano, como lavar o quintal e carro e para regar as plantas;
<br /><br />
- A televisão representa de 5% a 15% do consumo de energia da sua residência. Procure desligá-la quando ninguém estiver assistindo ou quando for fazer alguma atividade fora do cômodo na qual ela está instalada;
<br /><br />
- Deixe o carro na garagem. Isso evita a queima de combustível e o lançamento de gás carbônico na atmosfera e, conseqüentemente, diminui o efeito estufa;
<br /><br />
- Ande a pé ou de bicicleta. Além de ser um ótimo exercício, ajudando no bom funcionamento do sistema cardiovascular, também é uma boa dica para deixar o carro em casa e poluir menos;
<br /><br />
- Use sacolas de pano ao invés das de plástico. Sacolas de plástico – quando jogadas nas vias públicas – entopem os esgotos e podem provocam enchentes;
<br /><br />
- Separe o lixo orgânico dos materiais que podem ser reciclados. Em casa, bastam duas lixeiras para você colaborar com o planeta. Numa delas, coloque o lixo orgânico (restos de comida) e na outra, os materiais que podem ser destinados à reciclagem: plásticos, papéis, metais e vidros;
<br /><br />
- Não jogue lâmpadas, pilhas, baterias (celular, automotivas e industriais), restos de tinta ou produtos químicos no lixo comum. Por conter substâncias nocivas podem contaminar o solo e lençóis freáticos. Faça o descarte deste tipo de produto em estabelecimentos que os comercializam ou na rede de assistência técnica autorizada.
