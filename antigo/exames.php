<span style="color:#333; font:12px/17px Arial, Helvetica, sans-serif;">
	<span style="font-size:13px"><b>- ULTRASONOGRAFIA</b></span>
    <br />
    A ultrassonografia (ou ecografia) é um método inócuo e relativamente barato de produzir em tempo real imagens em movimento das estruturas e órgãos do interior do corpo. Em virtude de ser um exame de realização muito simples, costuma ser usado para fins preventivos, diagnósticos ou como acompanhamento de tratamentos. Através do efeito doppler (nome dado em homenagem a Johann Christian Andreas Doppler, seu criador), a ultrassonografia permite também detectar o sentido e a velocidade da corrente sanguínea em determinado segmento do corpo. É o método ideal para examinar mulheres gestantes, durante o acompanhamento pré-natal, permitindo reconhecer o sexo do bebê antes do nascimento, bem como diagnosticar eventuais alterações morfológicas ou funcionais do feto, realizar intervenções intrauterinas e prever as que serão necessárias após o nascimento.
     <br /><br /> 
      <span style="font-size:13px"><b>- RADIOLOGIA</b></span>
    <br />
    A Radiologia é o ramo ou a especialidade da medicina que trata o estudo e a aplicação da tecnologia imagiológica como o raio X e a radiação a diagnosticar e a tratar a doença.
     <br /><br /> 
      <span style="font-size:13px"><b>- MAMOGRAFIA</b></span>
    <br />
    A mamografia é a melhor técnica para diagnosticar câncer de mama em mulheres acima de 35 anos. Trata-se de uma radiografia cuja imagem obtida ajuda a detectar alterações na mama. 
     <br /><br /> 
      <span style="font-size:13px"><b>- TOMOGRAFIA</b></span>
    <br />
    A tomografia computadorizada (TC) é um dos métodos de exame mais confiáveis e seguros disponíveis atualmente. É rápida, simples e totalmente indolor. A TC utiliza um aparelho de raios X que gira a sua volta, fazendo radiografias transversais de seu corpo.
    Estas radiografias são então convertidas por um computador nos chamados cortes tomográficos. Isto quer dizer que a TC constrói imagens internas das estruturas do corpo e dos órgãos através de cortes transversais, de uma série de seções fatiadas que são posteriormente montadas pelo computador para formar um quadro completo. Portanto, com a TC o interior de seu corpo pode ser retratado com precisão e confiança para ser depois examinado.
